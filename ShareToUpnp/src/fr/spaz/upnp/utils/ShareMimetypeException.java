package fr.spaz.upnp.utils;

public class ShareMimetypeException extends Exception
{

	public ShareMimetypeException()
	{
		super();
	}

	public ShareMimetypeException(String detailMessage, Throwable throwable)
	{
		super(detailMessage, throwable);
	}

	public ShareMimetypeException(String detailMessage)
	{
		super(detailMessage);
	}

	public ShareMimetypeException(Throwable throwable)
	{
		super(throwable);
	}

}
